-- Create SL_USERS Table
CREATE TABLE SL_USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(100) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    PASSWORD VARCHAR2(255) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    EXPO_PUSH_TOKEN VARCHAR2(255)
);

-- Create SL_WALLETS Table
CREATE TABLE SL_WALLETS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    BALANCE NUMBER(15, 2) DEFAULT 10000.00, -- Default virtual money
    CURRENCY VARCHAR2(10) DEFAULT 'USD',
    LAST_UPDATED TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_USER_WALLET FOREIGN KEY (USER_ID) REFERENCES SL_USERS(ID)
);

-- Create SL_ALERTS Table
CREATE TABLE SL_ALERTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    SYMBOL VARCHAR2(20) NOT NULL,
    TARGET_PRICE NUMBER(20, 8) NOT NULL,
    ALERT_TYPE VARCHAR2(10) NOT NULL, -- 'ABOVE' or 'BELOW'
    IS_ACTIVE NUMBER(1) DEFAULT 1, -- 1 for active, 0 for triggered/disabled
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_USER_ALERT FOREIGN KEY (USER_ID) REFERENCES SL_USERS(ID)
);

-- Index for faster lookups
CREATE INDEX IDX_USERS_EMAIL ON SL_USERS(EMAIL);
CREATE INDEX IDX_ALERTS_USER ON SL_ALERTS(USER_ID);
CREATE INDEX IDX_ALERTS_ACTIVE ON SL_ALERTS(IS_ACTIVE);

-- Create SL_TRANSACTIONS Table
CREATE TABLE SL_TRANSACTIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER NOT NULL,
    AMOUNT NUMBER(15, 2) NOT NULL,
    TYPE VARCHAR2(20) NOT NULL, -- 'DEPOSIT', 'WITHDRAW', 'BUY', 'SELL'
    STATUS VARCHAR2(20) DEFAULT 'COMPLETED',
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_USER_TX FOREIGN KEY (USER_ID) REFERENCES SL_USERS(ID)
);

CREATE INDEX IDX_TX_USER ON SL_TRANSACTIONS(USER_ID);
